		<h2 id="chap1"> Origines </h2>
<p>
Le jazz Manouhe à l'origine puisqu'il s'agit d'en convenir est né chez les manouches, hein, c'est-à-dire les peuples un peu moins à l'ouest que ceux d'Europe occidentale. Puis évidemment, vu le succès de ses poum-poum-tchac à la guitare, une série de gens complètement à l'ouest ont commencé à reproduire les poum-poum-tchac. Certains ont très vite jugé la reproduction comme étant le fruit d'une usurpation d'identité. Ils s'exclamaient entre autre "Qu'ils aillent ailleur ces gitans, ici on joue du Belge!" mais d'autres ont décidé de faire valoir leur caractère indigène préférant être assimilés à une bande de vauriens qu'à la fange bourgeoise assises sur son gros séant. Ils disaient "qu'ils aillent se faire voir ces gros réacs". </p> 

